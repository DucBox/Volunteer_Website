from fastapi import FastAPI, Request
from fastapi.middleware.cors import CORSMiddleware
from slowapi import Limiter, _rate_limit_exceeded_handler
from slowapi.util import get_remote_address
from slowapi.errors import RateLimitExceeded
from app.api.routes import documents, chat

limiter = Limiter(key_func=get_remote_address)

app = FastAPI(title="RAG Chatbot API", version="1.0.0")

app.state.limiter = limiter
app.add_exception_handler(RateLimitExceeded, _rate_limit_exceeded_handler)

# CORS configuration with your Vercel domain
app.add_middleware(
    CORSMiddleware,
    allow_origins=[
        "https://volunteer-website-self.vercel.app",  # Production
        "https://admin-dashboard-em-lilac.vercel.app/",                      
        # "http://127.0.0.1:8000",                     # Local testing
    ],
    allow_credentials=True,
    allow_methods=["GET", "POST", "PUT", "DELETE"],
    allow_headers=["Content-Type", "Authorization"],
)

app.include_router(documents.router)
app.include_router(chat.router)


@app.get("/health")
async def health_check():
    return {"status": "ok"}